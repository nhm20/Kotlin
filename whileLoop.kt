fun main(){
    //while loop
    var i=5;
    while(i>0){
        print("$i ")
        i--
    }
    println()

    //do while
    i=5;
    do{
        print("value: $i ")
        i--;
    }
    while(i>0)
    println()

    i=1;
    do{
        print("value: $i ")
        i++;
    }
    while(i<=5)
    println()

}



